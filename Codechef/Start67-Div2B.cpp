const int N = 1e5 + 9;
vector<int> adj[N], dist;

void bfs(int s)
{
    dist.assign(N + 1, -1);
    queue<int> q;
    dist[s] = 0;
    q.push(s);
    while (q.size())
    {
        int u = q.front();
        q.pop();
        for (int v : adj[u])
        {
            if (dist[v] == -1)
            {
                dist[v] = dist[u] + 1;
                q.push(v);
            }
        }
    }
}

void solve()
{
    int n;
    cin >> n;
    int x1, y1, x2, y2;
    cin >> x1 >> y1 >> x2 >> y2;
    int top = min(x1, n - x1 + 1);
    int left = min(y1, n - y1 + 1);
    int right = min(x2, n - x2 + 1);
    int bottom = min(y2, n - y2 + 1);
    int total = abs(x2 - x1) + abs(y2 - y1);
    cout << min(total, min(right, bottom) + min(top, left)) << endl;
}

